package classifiermachine;

/**
 * Enumerated type capturing the machine operations.
 * This is used as the 'type' of the operations.
 */
public enum MachineOperations {
  /**
   * Representing moveInside machine operation.
   */
  MOVEINSIDE,

  /**
   * Representing moveOutside machine operation.
   */
  MOVEOUTSIDE,

  /**
   * Representing pressButton machine operation.
   */
  PRESSBUTTON
}
